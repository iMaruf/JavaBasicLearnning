package com.learning.algorithm;

public class Math {


	public static double addTwoNumbers(double num1, double num2) {
			double sum;
			sum = num1+num2;
			return sum;

	}
	
	public static double subtractSecondFormFirst(double num1, double num2) {
		double sub;
		sub = num1-num2;
		return sub;

	}
	
	public static double multiplyTwoNumbers(double num1, double num2) {
		double mult;
		mult = num1*num2;
		return mult;

	}
	
	public static double divideFirstBySecond(double num1, double num2) {
		double div;
		div = num1/num2;
		return div;

	}
	
	public static double modulusFirstToSecond(double num1, double num2){
		double mod;
		mod = num1%num2;
		return mod;
	}


}
